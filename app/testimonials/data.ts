// Utility functions
export function getTestimonialById(id: string): Testimonial | undefined {
  return testimonials.find(t => t.id === id);
}

export function getFeaturedTestimonials(limit: number = 6): Testimonial[] {
  return testimonials
    .filter(t => t.featured)
    .slice(0, limit);
}

export function getTestimonialsByCategory(category: string): Testimonial[] {
  if (category === 'All') return testimonials;
  return testimonials.filter(t => t.category === category);
}

export function getTestimonialStats() {
  const totalTestimonials = testimonials.length;
  const averageRating = testimonials.reduce((sum, t) => sum + t.rating, 0) / totalTestimonials;
  const verifiedCount = testimonials.filter(t => t.verified).length;
  const countries = [...new Set(testimonials.map(t => t.country))].length;

  return {
    total: totalTestimonials,
    averageRating: averageRating.toFixed(1),
    verified: verifiedCount,
    countries,
  };
}
