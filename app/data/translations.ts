/**
 * Get translation for a given text and language code
 * @param text - The text to translate
 * @param langCode - The target language code
 * @returns The translated text or original text if no translation exists
 */
export function getTranslation(text: string, langCode: string): string {
  if (langCode === 'en' || !text) {
    return text;
  }

  const translation = translations[text]?.[langCode];
  return translation || text;
}
